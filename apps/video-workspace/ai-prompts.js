function stageGuard(kind){
  const guards = {
    analysis:'允许并鼓励主动查资料；禁止使用或假装已有：AI 分析结果、03结构、04文案、06发布包、07复盘。必须先整理证据池，再给结论。',
    outline:'允许补查结构所需资料；不要写完整口播稿，不要写发布标题，不要做多语言。每一节必须绑定证据编号和可拍素材。',
    script:'允许为事实核验补查资料；不要生成发布标题/简介/标签，不要做复盘。每段口播必须能追溯到证据、结构目标或明确的个人判断。',
    platform:'允许查平台关键词、同类标题和社区搜索词；不要重写整篇脚本。标题必须匹配脚本和证据边界，不得夸大。',
    translate:'允许查各语言通用译名、海外社区说法和 SEO 关键词；不要改选题核心，不要把中文梗硬翻。',
    review:'允许对照平台数据/评论/同类内容经验做诊断；不要补写脚本或发布包。数据不足时先输出缺口，不要装作能复盘。'
  };
  return guards[kind] || '';
}
